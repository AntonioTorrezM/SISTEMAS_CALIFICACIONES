package com.emergentes.modelo;

public class Materias {
   
    private int id;
    private String materia;

    public Materias() {
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public String getMateria() {
        return materia;
    }

    public void setMateria(String materia) {
        this.materia = materia;
    }
}
